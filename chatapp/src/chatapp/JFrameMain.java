    private void startReceiveMessage() {

        try {
            receiverRunner.start();
            Thread thread = new Thread() {
                public void run() {
                    try {
                        while (receiverRunner.isRunning()) {
                            //System.out.println("dome");
                            if (receiverRunner.hasMessage()) {
                                
                                jTextAreaMainMessageContent.append(receiverRunner.getMessage());
                                jTextAreaMainMessageContent.append("\n");
                                jTextAreaMainMessageContent.append("\n");
                                jTextAreaMainMessageContent.setCaretPosition(jTextAreaMainMessageContent.getDocument().getLength());
                            }
                        }
                    } catch (Exception e) {
                    }
                }
            };
            thread.start();
        } catch (Exception e) {
        }
    }

