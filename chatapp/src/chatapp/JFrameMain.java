    private void jButtonConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConnectActionPerformed
        try {
            if (!jTextFieldAddress.getText().equalsIgnoreCase("") && !jTextFieldPort.getText().equalsIgnoreCase("")) {
                receiverRunner = new ReceiverRunner(jTextFieldAddress.getText(), Integer.parseInt(jTextFieldPort.getText()),jTextFieldNetworkInterface.getText().trim());
                senderRunner = new SenderRunner(jTextFieldAddress.getText(), Integer.parseInt(jTextFieldPort.getText()),jTextFieldNetworkInterface.getText().trim());
            } else {
                receiverRunner = new ReceiverRunner();
                senderRunner = new SenderRunner();
            }
        } catch (NumberFormatException e) {
            receiverRunner = new ReceiverRunner();
            senderRunner = new SenderRunner();
        }

        try {
            if (receiverRunner.isConnected() && senderRunner.isConnected()) {
                jLabelStatus.setVisible(false);
                startReceiveMessage();
            } else {
                jLabelStatus.setVisible(true);
            }
        } catch (Exception e) {
            jLabelStatus.setVisible(true);
        }
        jDialogConnect.setVisible(false);
    }//GEN-LAST:event_jButtonConnectActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String name;
            if(jTextFieldName.getText().equalsIgnoreCase("")){
                name = "An danh";
            }else{
                name = jTextFieldName.getText();
            }
            name = "["+name+"] "+ " : ";
            if (!jTextFieldMessage.getText().equalsIgnoreCase("")) {
                senderRunner.send(name+jTextFieldMessage.getText());
                jTextFieldMessage.setText("");
                jTextAreaMainMessageContent.setCaretPosition(jTextAreaMainMessageContent.getDocument().getLength());
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

